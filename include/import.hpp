#define GLUE(x, y) x y

#define RETURN_ARG_COUNT(_1_, _2_, _3_, _4_, _5_, _6_, _7_, _8_, count, ...) count
#define EXPAND_ARGS(args) RETURN_ARG_COUNT args
#define COUNT_ARGS_MAX8(...) EXPAND_ARGS((__VA_ARGS__, 8, 7, 6, 5, 4, 3, 2, 1, 0))

#define OVERLOAD_MACRO2(name, count) name##count
#define OVERLOAD_MACRO1(name, count) OVERLOAD_MACRO2(name, count)
#define OVERLOAD_MACRO(name, count) OVERLOAD_MACRO1(name, count)

#define CALL_OVERLOAD(name, ...) GLUE(OVERLOAD_MACRO(name, COUNT_ARGS_MAX8(__VA_ARGS__)), (__VA_ARGS__))

#define BOOST_LOCAL_BIND_NAMESPACE_SELECT_2(name, modifier, ...) name
#define BOOST_LOCAL_BIND_NAMESPACE_SELECT_(...) BOOST_LOCAL_BIND_NAMESPACE_SELECT_2(__VA_ARGS__,)
#define BOOST_LOCAL_BIND_NAMESPACE_SELECT2(name, modifier, ...) name ::
#define BOOST_LOCAL_BIND_NAMESPACE_SELECT(...) BOOST_LOCAL_BIND_NAMESPACE_SELECT2(__VA_ARGS__,)
#define BOOST_LOCAL_BIND_NAMESPACE_EXPAND8(a, ...) BOOST_LOCAL_BIND_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_EXPAND7(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_EXPAND7(a, ...) BOOST_LOCAL_BIND_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_EXPAND6(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_EXPAND6(a, ...) BOOST_LOCAL_BIND_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_EXPAND5(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_EXPAND5(a, ...) BOOST_LOCAL_BIND_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_EXPAND4(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_EXPAND4(a, ...) BOOST_LOCAL_BIND_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_EXPAND3(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_EXPAND3(a, ...) BOOST_LOCAL_BIND_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_EXPAND2(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_EXPAND2(a, ...) BOOST_LOCAL_BIND_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_EXPAND1(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_EXPAND1(a) BOOST_LOCAL_BIND_NAMESPACE_SELECT_ a
// Expands into a::b::c:: ...
#define BOOST_LOCAL_BIND_NAMESPACE(...) CALL_OVERLOAD(BOOST_LOCAL_BIND_NAMESPACE_EXPAND,__VA_ARGS__)

#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT2(name, modifier, ...) modifier namespace name {
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT(...) BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT2(__VA_ARGS__,)
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND8(a, ...) BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND7(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND7(a, ...) BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND6(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND6(a, ...) BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND5(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND5(a, ...) BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND4(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND4(a, ...) BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND3(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND3(a, ...) BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND2(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND2(a, ...) BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT a BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND1(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND1(a) BOOST_LOCAL_BIND_NAMESPACE_BEGIN_NAMESPACE_SELECT a

// Expands into namespace a { namespace b { namespace c ...
#define BOOST_LOCAL_BIND_NAMESPACE_BEGIN(...) CALL_OVERLOAD(BOOST_LOCAL_BIND_NAMESPACE_BEGIN_EXPAND,__VA_ARGS__)

#define BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND8(a, ...) } BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND7(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND7(a, ...) } BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND6(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND6(a, ...) } BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND5(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND5(a, ...) } BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND4(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND4(a, ...) } BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND3(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND3(a, ...) } BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND2(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND2(a, ...) } BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND1(__VA_ARGS__)
#define BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND1(a) }

// Expands into } } ...
#define BOOST_LOCAL_BIND_NAMESPACE_END(...) CALL_OVERLOAD(BOOST_LOCAL_BIND_NAMESPACE_END_EXPAND,__VA_ARGS__)

#endif
